public class Vector2Int {

    private int x = 0;
    private int y = 0;

    public Vector2Int(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return this.x;
    }

    public int getY()
    {
        return this.y;
    }

}
